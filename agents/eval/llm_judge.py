"""LLM-as-judge for story quality evaluation (M2)."""

from __future__ import annotations

import json
import logging
from typing import Any

from openai import OpenAI
from tenacity import retry, stop_after_attempt, wait_exponential, retry_if_exception_type
import openai as openai_mod

logger = logging.getLogger(__name__)

_client: OpenAI | None = None


def _get_client() -> OpenAI:
    global _client
    if _client is None:
        _client = OpenAI()
    return _client


QUALITY_RUBRIC = """Score each dimension 1-5:

### Clarity (1-5)
5: Title and description are precise, unambiguous, single-purpose
4: Clear and understandable, minor rewording would improve
3: Generally clear but some ambiguous language
2: Vague, could be interpreted multiple ways
1: Unclear, would need significant rewriting

### Actionability (1-5)
5: Developer could implement immediately without questions
4: Mostly implementable, 1-2 minor clarifications needed
3: Implementable with some assumptions
2: Too vague to implement without significant clarification
1: Not actionable — more of a wish than a requirement

### Completeness (1-5)
5: Captures 100% of the requirement from source text
4: Captures 80%+, minor details missing
3: Captures the core but misses important details
2: Captures only part of the requirement
1: Misses the main point of the requirement

### Acceptance Criteria Quality (1-5)
5: All criteria are specific, testable, and sufficient for the story
4: Most criteria are testable, 1 could be more specific
3: Criteria exist but some are vague or untestable
2: Criteria are too generic to be useful
1: Missing or completely irrelevant criteria
"""

JUDGE_SYSTEM = f"""You are evaluating a user story generated by an AI system from a meeting transcript.

{QUALITY_RUBRIC}

Output JSON:
{{
  "clarity": {{"score": N, "rationale": "..."}},
  "actionability": {{"score": N, "rationale": "..."}},
  "completeness": {{"score": N, "rationale": "..."}},
  "acceptance_criteria": {{"score": N, "rationale": "..."}},
  "overall": N.N
}}

The "overall" score should be the average of the four dimension scores.
Return ONLY valid JSON.
"""


@retry(
    stop=stop_after_attempt(3),
    wait=wait_exponential(multiplier=1, min=2, max=30),
    retry=retry_if_exception_type((openai_mod.RateLimitError, openai_mod.APITimeoutError)),
)
async def llm_judge(story: dict) -> dict:
    """
    Use GPT-4o as a judge to score a single story on four quality dimensions.

    Args:
        story: dict with keys: source_citation, title, type, description,
               acceptance_criteria, feature_tags

    Returns:
        dict with scores for clarity, actionability, completeness,
        acceptance_criteria, and overall.
    """
    user_content = f"""Source text from transcript:
"{story.get('source_citation', 'N/A')}"

Generated story:
Title: {story.get('title', '')}
Type: {story.get('type', '')}
Description: {story.get('description', '')}
Acceptance Criteria: {json.dumps(story.get('acceptance_criteria', []))}
Feature Tags: {json.dumps(story.get('feature_tags', []))}
"""

    resp = _get_client().chat.completions.create(
        model="gpt-5.4-mini",
        messages=[
            {"role": "system", "content": JUDGE_SYSTEM},
            {"role": "user", "content": user_content},
        ],
        temperature=0.0,
        response_format={"type": "json_object"},
    )
    content = resp.choices[0].message.content or "{}"
    data = json.loads(content)

    # Ensure overall is computed
    dims = ["clarity", "actionability", "completeness", "acceptance_criteria"]
    scores = []
    for d in dims:
        if d in data and isinstance(data[d], dict):
            scores.append(data[d].get("score", 3))
    if scores:
        data["overall"] = sum(scores) / len(scores)
    elif "overall" not in data:
        data["overall"] = 3.0

    return data
